package at.technikum.EscapeCharacters;

public class Main {
    public static void main(String[] args) {
        System.out.println("escape sequence is started with a backslash: \\");
        System.out.println("to print a tab (\t) use \"\\t\"");
        System.out.println("to print a backslash (\\) use \"\\\\\"");
        System.out.println("to print double quotes (\") use \"\\\"\"");
        System.out.println("to print a single quote (\') use \"\\\'\"");
        System.out.println("to start a new line (\n) use \"\\n\"");
    }
}
