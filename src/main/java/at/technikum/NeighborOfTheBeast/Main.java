package at.technikum.NeighborOfTheBeast;

public class Main {
    public static void main(String[] args) {
        Integer lenumber = 667;

        System.out.printf("the neighbor of the beast: %d!\n", lenumber);
        System.out.printf("the neighbor of the beast: %H!\n", lenumber);
        System.out.printf("the neighbor of the beast: %10d!\n", lenumber);
        System.out.printf("the neighbor of the beast: %-10d!\n", lenumber);
        System.out.printf("the neighbor of the beast: %08d!\n", lenumber);
        System.out.printf("the neighbor of the beast: %+-12d!\n", lenumber);

    }
}
